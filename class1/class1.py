# 基本型態 basic types
print(1)  # 整數 int
print(1.1)  # 浮點數 float, 只要有小數點就是浮點數
print("hello")  # 字串 str, 用雙引號""或單引號''包起來的都是字串
print(True)  # 布林值 bool, 第一個字母要大寫
print(None)  # 空值 NoneType

# 變數 variable
a = 1  # 把1這個整數存到電腦的記憶體中，並且取名為a
# = 的功能是將右邊的資料存到左邊的變數空間當中
print(a)  # 把a這個變數的值印出來
